
#include "OperationDeBase.h"

int somme (int premierValeur, int secondValeur){
    return premierValeur+secondValeur;
}

int produit (int premierValeur, int secondValeur){
    return premierValeur*secondValeur;
}

int difference (int premierValeur, int secondValeur){
    return premierValeur-secondValeur;
}

int divisionEntier (int premierValeur, int secondValeur){
    return premierValeur/secondValeur;
}
